// Writes src/generated/build-info.ts with the package version and build date
// baked in at build time. The update check reads these: BUILD_VERSION for the
// "newer version available" compare, BUILD_DATE for the age-based fallback
// notice. The file is committed so tests and dev runs work without a build.
import { mkdirSync, readFileSync, writeFileSync } from "node:fs"
import { join } from "node:path"

const { version } = JSON.parse(readFileSync(join(process.cwd(), "package.json"), "utf8"))
const date = new Date().toISOString().slice(0, 10)

const dir = join(process.cwd(), "src", "generated")
mkdirSync(dir, { recursive: true })
writeFileSync(
  join(dir, "build-info.ts"),
  `// Generated by scripts/gen-build-info.mjs — do not edit.
export const BUILD_VERSION = "${version}"
export const BUILD_DATE = "${date}"
`,
)
console.error(`gen-build-info: v${version} (${date})`)
