// Writes src/generated/build-flavor.ts. Default flavor is "standard";
// FENEK_BUILD_FLAVOR=freepro bakes the self-build Pro flavor in at COMPILE
// time (see docs/building-pro.md). CI never sets this variable.
import { mkdirSync, writeFileSync } from "node:fs"
import { join } from "node:path"

const raw = process.env.FENEK_BUILD_FLAVOR
const flavor = raw === undefined || raw === "" ? "standard" : raw
if (flavor !== "standard" && flavor !== "freepro") {
  console.error(`gen-build-flavor: unknown flavor '${flavor}' (expected standard or freepro)`)
  process.exit(1)
}

const dir = join(process.cwd(), "src", "generated")
mkdirSync(dir, { recursive: true })
writeFileSync(
  join(dir, "build-flavor.ts"),
  `// Generated by scripts/gen-build-flavor.mjs — do not edit.
export type BuildFlavor = "standard" | "freepro"
export const BUILD_FLAVOR: BuildFlavor = "${flavor}"
`,
)
console.error(`gen-build-flavor: ${flavor}`)
